// Helper function to get translation by key path
export function t(keyPath: string): string {
  const keys = keyPath.split(".");
  let value: unknown = ruTranslations;

  for (const key of keys) {
    if (value && typeof value === "object" && key in value) {
      value = (value as Record<string, unknown>)[key];
    } else {
      return keyPath; // Return key path if not found
    }
  }

  return typeof value === "string" ? value : keyPath;
}

// Helper function to get skill name in Russian
export function getSkillNameRu(skillId: string): string {
  // Нормализуем ключ (поддержка обоих форматов)
  const key = skillId.toLowerCase();
  return (
    ruTranslations.skills[key as keyof typeof ruTranslations.skills] ||
    ruTranslations.skills[
      key.replace(/_/g, "-") as keyof typeof ruTranslations.skills
    ] ||
    skillId
  );
}

// Helper function to get ability name in Russian
export function getAbilityNameRu(ability: string): string {
  return (
    ruTranslations.abilities[
      ability.toLowerCase() as keyof typeof ruTranslations.abilities
    ] || ability
  );
}

// Helper function to get ability abbreviation in Russian
export function getAbilityAbbr(ability: string): string {
  const abbrMap: Record<string, string> = {
    strength: "СИЛ",
    dexterity: "ЛОВ",
    constitution: "ТЕЛ",
    intelligence: "ИНТ",
    wisdom: "МДР",
    charisma: "ХАР",
  };
  return (
    abbrMap[ability.toLowerCase()] || ability.substring(0, 3).toUpperCase()
  );
}
